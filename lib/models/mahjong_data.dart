import 'package:flutter/material.dart';

// Learning Stage
class LearningStage {
  final String id;
  final String title;
  final String subtitle;
  final IconData icon;
  final Color color;
  final int lessonCount;
  bool isUnlocked;
  int completedLessons;

  LearningStage({
    required this.id,
    required this.title,
    required this.subtitle,
    required this.icon,
    required this.color,
    required this.lessonCount,
    this.isUnlocked = false,
    this.completedLessons = 0,
  });

  double get progress =>
      lessonCount > 0 ? completedLessons / lessonCount : 0;
}

// Dialogue message
class DialogueMessage {
  final String speaker; // 'mj' (Panda MJ) or 'user'
  final String text;
  final Widget? widget;
  final List<String>? tileIds;

  DialogueMessage({required this.speaker, required this.text, this.widget, this.tileIds});
}

// Fan type definition
class FanType {
  final String id;
  final String name;
  final String nameZh;
  final int score;
  final String scoreLabel;
  final List<String> tileIds;
  final String description;
  final String example;

  const FanType({
    required this.id,
    required this.name,
    required this.nameZh,
    required this.score,
    required this.scoreLabel,
    required this.tileIds,
    required this.description,
    required this.example,
  });
}

// Lesson
class Lesson {
  final String id;
  final String stageId;
  final String title;
  final String subtitle;
  final List<DialogueMessage> dialogues;
  final List<QuizQuestion> questions;

  const Lesson({
    required this.id,
    required this.stageId,
    required this.title,
    required this.subtitle,
    required this.dialogues,
    required this.questions,
  });
}

// Quiz question
class QuizQuestion {
  final String question;
  final List<String> options;
  final int correctIndex;
  final String? explanation;
  final QuizType type;
  final List<String>? tiles;

  QuizQuestion({
    required this.question,
    required this.options,
    required this.correctIndex,
    this.explanation,
    this.type = QuizType.multiChoice,
    this.tiles,
  });
}

enum QuizType { multiChoice, tileSelection, tileOrdering }

// Leaderboard user
class LeaderboardUser {
  final String name;
  final String avatar;
  final int xp;
  final int rank;

  LeaderboardUser({
    required this.name,
    required this.avatar,
    required this.xp,
    required this.rank,
  });
}

